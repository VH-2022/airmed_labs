
What is a dummy text?

A dummy text is a text which looks like a particular language at first sight and which also has some aspects in common, but which doesn’t make any sense.
Who needs dummy texts?

Designer at work who don’t have any content for their product yet have the possibility to insert a dummy text into their design to judge on the arrangement of text on their site, on readability or on fonts and sizes. A dummy text is also helpful to present a design without content to a client to show how the text is going to look like without irritating the client by real texts.
Why another dummy text generator?

SO far, there were only generators for “Lorem ipsum” (a dummy text in latin) and some other languages, but there were only few European ones. For European and American designers, who probably would like to use dummy texts in their own language, we developed a generator for many European languages. On top of that it’s possible to upload your own language.
Why doesn’t the generator put as many characters as I told him?

In order not to stint the last word, this word is being finished. Then a full stop is added to make the text as real as possible.
What means “Print special chars as HTML entities”?

If you are writing HTML and you don’t use a programm which does this for you, you should replace “ä”, “ü” and und “ö” by “&auml;”, “&uuml;” and “&ouml;”, because it’s possible that these characters are sometimes not displayed correctly. If “Print special chars as HTML entities” is checked, those special characters like the German umlauts are automatically changed to HTML entities (the strings with the & and the ;) .
What is a template?

Templates are texts in particular language, which the generator needs to analyse the language and to create dummy texts. There are default templates we supplied but you are also able to upload your own ones.
Why is a template text recommended to be as long as possible?

To analyse the language well, the generator needs a long text in the particular language. So he’s able to identify the language characteristics better and the dummy text is more authentic.
Why should I upload own templates?

If you need other languages than the default ones, you can look for a text in that language, paste it into the text field or save and upload it. Then you’re able to create dummy texts of any length in that language. Of course, we are delighted in each new language for our generator.
